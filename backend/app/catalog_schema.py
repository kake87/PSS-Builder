def _default_type_definition(type_key: str) -> EquipmentTypeDefinition:
    return EquipmentTypeDefinition(
        id=f"type-{type_key}",
        key=type_key,
        name=type_key.replace("_", " ").title(),
        required_fields=["name", "device_type", "model", "manufacturer", "ports"],
        lifecycle_status="draft",
        description="Autogenerated type definition.",
    )


def _make_compatibility_rules() -> List[CompatibilityRuleDefinition]:
    return [
        CompatibilityRuleDefinition(
            id="rule-eth-eth",
            rule_key="ethernet_to_ethernet",
            from_port_type="ethernet",
            to_port_type="ethernet",
            allowed_cables=["CAT5e", "CAT6", "OPTICAL"],
            max_length_meters=100,
        ),
        CompatibilityRuleDefinition(
            id="rule-serial-serial",
            rule_key="serial_to_serial",
            from_port_type="serial",
            to_port_type="serial",
            allowed_cables=["SERIAL"],
            max_length_meters=15,
        ),
        CompatibilityRuleDefinition(
            id="rule-power-power",
            rule_key="power_to_power",
            from_port_type="power",
            to_port_type="power",
            allowed_cables=["POWER"],
            max_length_meters=30,
        ),
    ]


def build_normalized_catalog(equipment_catalog: Dict[str, Device]) -> NormalizedCatalogResponse:
    type_keys = sorted({str(device.device_type) for device in equipment_catalog.values()})
    equipment_types: List[EquipmentTypeDefinition] = [
        TYPE_DEFINITIONS.get(type_key, _default_type_definition(type_key)) for type_key in type_keys
    ]

    equipment_models: List[EquipmentModelDefinition] = []
    for key, device in equipment_catalog.items():
        equipment_models.append(
            EquipmentModelDefinition(
                id=str(uuid4()),
                key=key,
                type_key=str(device.device_type),
                name=device.name,
                manufacturer=device.manufacturer,
                model=device.model,
                power_consumption_watts=device.power_consumption_watts,
                resolution=device.resolution,
                storage_capacity_gb=device.storage_capacity_gb,
                bandwidth_requires_mbps=device.bandwidth_requires_mbps,
                ports=[
                    CatalogPortDefinition(
                        name=port.name,
                        port_type=str(port.port_type),
                        speed_mbps=port.speed_mbps,
                        power_watts=port.power_watts,
                    )
                    for port in device.ports
                ],
            )
        )

    return NormalizedCatalogResponse(
        generated_at=datetime.now(timezone.utc).isoformat(),
        equipment_types=equipment_types,
        equipment_models=equipment_models,
        compatibility_rules=_make_compatibility_rules(),
    )
