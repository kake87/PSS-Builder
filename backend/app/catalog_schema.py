"""
Normalized catalog schema and builders.
"""
from datetime import datetime, timezone
from typing import Dict, List, Optional
from uuid import uuid4

from pydantic import BaseModel, Field

from app.models import Device
from app.version import CATALOG_SCHEMA_VERSION


class EquipmentTypeDefinition(BaseModel):
    id: str
    key: str
    name: str
    required_fields: List[str] = Field(default_factory=list)
    lifecycle_status: str = "verified"
    description: Optional[str] = None


class CatalogPortDefinition(BaseModel):
    name: str
    port_type: str
    speed_mbps: Optional[int] = None
    power_watts: Optional[float] = None


class EquipmentModelDefinition(BaseModel):
    id: str
    key: str
    type_key: str
    name: str
    manufacturer: str
    model: str
    lifecycle_status: str = "verified"
    schema_version: str = CATALOG_SCHEMA_VERSION
    power_consumption_watts: Optional[float] = None
    resolution: Optional[str] = None
    storage_capacity_gb: Optional[int] = None
    bandwidth_requires_mbps: Optional[int] = None
    ports: List[CatalogPortDefinition] = Field(default_factory=list)


class CompatibilityRuleDefinition(BaseModel):
    id: str
    rule_key: str
    from_port_type: str
    to_port_type: str
    allowed_cables: List[str] = Field(default_factory=list)
    max_length_meters: Optional[int] = None
    lifecycle_status: str = "verified"


class NormalizedCatalogResponse(BaseModel):
    schema_version: str = CATALOG_SCHEMA_VERSION
    generated_at: str
    equipment_types: List[EquipmentTypeDefinition]
    equipment_models: List[EquipmentModelDefinition]
    compatibility_rules: List[CompatibilityRuleDefinition]


